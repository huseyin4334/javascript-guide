/*
    data-* attributes:
    - https://developer.mozilla.org/en-US/docs/Web/HTML/Global_attributes/data-*
    - We can set custom attributes to HTML elements by using data-* attributes
    - We can access these attributes in JavaScript by using the dataset property
    - The dataset property is an object that contains all the data-* attributes
    - The dataset property is a DOMStringMap object
    - It's a live object, meaning that it's updated when the attributes change

*/